package model.objective;

public class SecretObjectiveGoal {
	
	private String name;
	private String link;
	
	
	public SecretObjectiveGoal(String name, String link) {
		this.name = name;
		this.link = link;
	}

	
	public String getName() {
		return name;
	}

	public String getLink() {
		return link;
	}

}
